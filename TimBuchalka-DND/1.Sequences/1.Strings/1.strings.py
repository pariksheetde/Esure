# NEW LINE CHARACTER
stmt = 'What\'s in a name. \nThis was quoted by William Shakespere'
print(stmt)
print()

# TAB SEPARATION
stmt = 'What\'s in a name. \tThis was quoted by William Shakespere'
print(stmt)
print()

# NEW LINE CHARACTER
splitstring = "This string has been \nsplit over \nseveral \nlines"
print(splitstring)
print()

# TAB SEPARATION
tabbedstring = '1\t2\t3\t4'
print(tabbedstring)
print()